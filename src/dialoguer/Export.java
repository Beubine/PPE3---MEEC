    private void jButton_ExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ExportActionPerformed
       
        if (!jTextnombateau.getText().equals("")){ 
            String unbateau = jTextnombateau.getText();
            try{
                Element bateau = new Element("Bateau");
                Document doc = new Document(bateau);
                bateau.setAttribute(new Attribute("nom",unbateau));            
            
                    for(int roww: jTable1.getSelectedRows()){
                        Element conteneur = new Element("conteneur");
                        String unnumcont= String.valueOf(jTable1.getValueAt(roww, 0));
                        String uneposition= String.valueOf(jTable1.getValueAt(roww, 2));
                        String unNumCli= String.valueOf(jTable1.getValueAt(roww,3 ));
                        String[] ary = uneposition.split("");
                        String unQuai = ary[0] + ary[1]; 
                        String uneAllee = ary[2] + ary[3];
                        String unEmplacement = ary[4] + ary[5];
                        String unEtage = ary[6];
                        //ajout de conteneur
                        conteneur.addContent(new Element("numero").setText(unnumcont));
                        conteneur.addContent(new Element("quai").setText(unQuai));
                        conteneur.addContent(new Element("allee").setText(uneAllee));
                        conteneur.addContent(new Element("emplacement").setText(unEmplacement));
                        conteneur.addContent(new Element("etage").setText(unEtage));
                        conteneur.addContent(new Element("client").setText(unNumCli));
                        doc.getRootElement().addContent(conteneur);
                
                
                    }
                XMLOutputter xml = new XMLOutputter();
                xml.setFormat(Format.getPrettyFormat());
                DateFormat format = new SimpleDateFormat("yyyy_MM_dd_HH_mm_ss");
                Calendar calendar = Calendar.getInstance();
                xml.output(doc, new FileWriter("ExportXML\\Rotterdam_"+ format.format(calendar.getTime()) +".xml"));
                JOptionPane.showMessageDialog(null, "Fichier enregistr√©.","CONFIRME", JOptionPane.YES_NO_CANCEL_OPTION);
                } catch(Exception e){
                    e.printStackTrace();
                }
        }else {
            JOptionPane.showMessageDialog(null, "Nom du bateau non remplie.","ERREUR", JOptionPane.ERROR_MESSAGE);
            jTextnombateau.requestFocus();
        }
        
        
    }//GEN-LAST:event_jButton_ExportActionPerformed

